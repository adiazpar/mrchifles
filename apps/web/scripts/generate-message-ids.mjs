#!/usr/bin/env node
/**
 * Generates apps/web/src/i18n/messageIds.d.ts from en-US.json.
 *
 * The emitted file does two things:
 *   1. Exports a `MessageId` union of every flat dot-key in the source-of-truth
 *      locale file, so consumers can `import type { MessageId } ...` if they
 *      need it (e.g. when typing a state variable that holds an id).
 *   2. Augments react-intl's global `FormatjsIntl.Message['ids']` type so that
 *      every `intl.formatMessage({ id })` and `<FormattedMessage id />` is
 *      typechecked against the union — a typo in an id becomes a compile error
 *      instead of a silent runtime fallback. This is the official react-intl
 *      typesafe-message-id pattern (formatjs.io/docs/react-intl/api).
 *
 * The script runs in two places:
 *   - `predev` hook on `apps/web` so editing en-US.json during dev refreshes
 *     the union the next time you start the dev server.
 *   - `prebuild` hook on `apps/web` so `tsc -b` in the build step catches new
 *     typos before they ship.
 *
 * Re-run manually any time with `npm run i18n:types --workspace=apps/web`.
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve, relative } from 'node:path'

const HERE = dirname(fileURLToPath(import.meta.url))
const WEB_ROOT = resolve(HERE, '..')
const SOURCE = resolve(WEB_ROOT, 'src/i18n/messages/en-US.json')
const OUTPUT = resolve(WEB_ROOT, 'src/i18n/messageIds.d.ts')

const messages = JSON.parse(readFileSync(SOURCE, 'utf8'))
const ids = Object.keys(messages).sort()

if (ids.length === 0) {
  throw new Error(
    `[i18n:types] no keys found in ${relative(WEB_ROOT, SOURCE)} — refusing to overwrite ${relative(WEB_ROOT, OUTPUT)} with an empty union`,
  )
}

// Single-quote the id; escape any embedded single quotes (none today, but the
// validator is cheap and prevents a future surprise).
const union = ids.map((id) => `  | '${id.replace(/'/g, "\\'")}'`).join('\n')

const file = `// AUTO-GENERATED by apps/web/scripts/generate-message-ids.mjs.
// DO NOT EDIT BY HAND. Re-run \`npm run i18n:types --workspace=apps/web\`
// (or rely on the predev / prebuild hook) after editing en-US.json.

export type MessageId =
${union}

declare global {
  namespace FormatjsIntl {
    interface Message {
      ids: MessageId
    }
  }
}

export {}
`

writeFileSync(OUTPUT, file)
console.log(
  `[i18n:types] wrote ${ids.length} ids -> ${relative(WEB_ROOT, OUTPUT)}`,
)
